cumprimento = "Olá"
nome = "Fernanda"
turno = "Boa tarde!"

tamanho_cumprimento=len(cumprimento)
tamanho_nome=len(nome)
tamanho_turno=len(turno)

print("O tamanho de cumprimento é: ", (tamanho_cumprimento))
print("O tamanho de nome é: ", (tamanho_nome))
print("O tamanho de turno é: ", (tamanho_turno))

tamanho_frase=len(cumprimento)+len(nome)+len(turno)
print("O tamanho da frase completa é: ",(tamanho_frase))
